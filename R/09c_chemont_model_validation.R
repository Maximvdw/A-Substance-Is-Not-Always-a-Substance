# ==============================================================================
# 09c_chemont_model_validation.R
# Model validation via structure-defined substances as ground truth
#
# PURPOSE
# -------
# Structure-defined substances have a known ChemOnt class via ClassyFire
# (encoded in data/processed/rdf/substances_taxonomy_levels.ttl via wk:
# typed hierarchy properties).  This provides an internal ground truth: for
# each such substance, we know which ChemOnt class is correct at each level.
#
# By embedding the *names* of structure-defined substances and ranking ChemOnt
# classes by cosine similarity, we can measure how often each model places the
# correct class in the top-1, top-3, and top-5 positions — a standard
# information retrieval metric (Hit@k).
#
# This is a principled comparison that does not require external labelled data:
# the ground truth is derived from structure-based classification (ClassyFire),
# which is independent of the embedding models being evaluated.
#
# DATA PROVENANCE
# ---------------
# Input: data/processed/chemont_ground_truth.csv
#   Generated by bash/chemont_broader_substance.sh via SPARQL against
#   data/processed/rdf/substances_taxonomy_levels.ttl.
#   Columns: inchikey, substance, substance_name, chemont_label (direct parent
#   class via skos:broader), subclass, class, superclass, kingdom (typed
#   hierarchy levels from wk: properties).
#   Hierarchy specificity: chemont_label > subclass > class > superclass > kingdom.
#
# METHODOLOGY
# -----------
# ## Established frameworks used as anchors
#
# | Framework | Role in methodology |
# |---|---|
# | **ChemOnt 2.1 / ClassyFire** (Djoumbou Feunang et al. 2016) | Ground-truth chemical taxonomy; defines the hierarchy levels |
# | **Hit@k / MRR** (standard IR metrics) | Retrieval performance at each taxonomy level |
#
# ## Own methodological additions
#
# | Choice | Justification |
# |---|---|
# | Per-level Hit@k (subclass/class/superclass/kingdom) | Each taxonomy level has different discriminative power; a single lenient metric hides where models fail in the hierarchy |
# | Weighted lenient MRR weights 1.00/0.75/0.50/0.25/0.10 | Reflect decreasing specificity moving up the ChemOnt hierarchy; weights are ordinal, not empirically calibrated |
#
# INTERPRETATION
# --------------
# 1. Strict Hit@k (direct parent): how often the model retrieves the exact
#    ClassyFire class in the top k — the hardest and most meaningful metric.
# 2. Per-level Hit@k: performance at each hierarchy level; higher levels
#    (kingdom, superclass) should always be easier than lower ones (subclass).
#    If a model scores better at kingdom than at subclass, that is expected.
#    If it scores poorly even at kingdom, the embedding is uninformative.
# 3. mrr_weighted: a single summary that rewards specificity — a kingdom-level
#    match at rank 1 is worth less than a subclass-level match at rank 2.
#    Models should be ranked by mrr_weighted, not mrr_strict alone.
#
# OUTPUTS
# -------
# output/figures/Analysis_9c_model_validation.pdf
# output/tables/Analysis_9c_model_validation.csv
# ==============================================================================

